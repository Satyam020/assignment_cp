#include <iostream>
using namespace std;

int main(){
    int a , b;
    cout<<"enter the first value"<<endl;
    cin>>a;
    cout<<"enter the second value"<<endl;
    cin>>b;
    cout<<"the sum of a+b is :"<<a+b<<endl;
    cout<<"the sum of a-b is :"<<a-b<<endl;
    cout<<"the sum of a*b is :"<<a*b<<endl;
    cout<<"the sum of a/b is :"<<a/b<<endl;
    cout<<"the sum of a%b is :"<<a%b<<endl;
    cout<<"the sum of a++ is :"<<a++<<endl;
    cout<<"the sum of ++a is :"<<++a<<endl;
    cout<<"the sum of b-- is :"<<b--<<endl;
    cout<<"the sum of --b is :"<<--b<<endl;
    return 0;
}
